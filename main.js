    function obtemAtributo() {
      let radioAtributo = document.getElementsByName("atributo")
      
      for (i=0; i < radioAtributo.length; i++) {
        if (radioAtributo[i].checked == true)
          return radioAtributo[i].value
      }
      
    }
     
